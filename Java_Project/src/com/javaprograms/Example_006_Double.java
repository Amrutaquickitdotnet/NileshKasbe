package com.javaprograms;

public class Example_006_Double {

	public static void main(String[] args) {
		double a = 31.25656;

		System.out.println(a);
		
		double b = 31.2565644;
		
		// Addition of 2 double numbers
		
		double result = a+b;
		System.out.println("Addition of 2 numbers :"+ result);
		
		
		double result1 = a-b;
		System.out.println("subtraction of 2 numbers :"+ result1);
		
		double result2 = a*b;
		System.out.println("Multiplication of 2 numbers :"+ result2);
		
		
		double result3 = a/b;
		System.out.println("division of 2 numbers :"+ result3);
		
		
		double result4 = a%b;
		System.out.println("Modulus of 2 numbers :"+ result4);
	}

}
