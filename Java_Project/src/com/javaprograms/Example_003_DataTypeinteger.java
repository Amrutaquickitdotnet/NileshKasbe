package com.javaprograms;

public class Example_003_DataTypeinteger {

	public static void main(String[] args) {
		// Data type variablename = "Values"
		// variable => is container which hold value

		// use shortcut keys ctrl+A then use ctrl+shift+F
		int a = 25;

		System.out.println("The value of a is " + a);

		int b = 13;

		int result = a + b;

		System.out.println("Addition of two numbers :" + result);

		int result1 = a - b;

		System.out.println("subtraction of two numbers :" + result1);

		int result2 = a * b;

		System.out.println("Multiplication of two numbers :" + result2);

		int result3 = a / b;

		System.out.println("division of two numbers :" + result3);
		
		int c = 4;
		int d = 2;

		int result4 = c % d;

		System.out.println("Modulus of two numbers :" + result4);

	}

}
